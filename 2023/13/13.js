/**
 * 
 * @param {string[]} singleArea 
 * @returns {number}
 */
const getStart = (singleArea) =>
    singleArea
        .map((line, i, wholeArr) =>
            line === wholeArr[i + 1] ? i : -1)
        .filter(num => num >= 0)
        .filter(lineNum =>
            checkMirror(singleArea, lineNum))
        .map(lineNum => lineNum + 1)[0] || 0

const checkMirror = (array, startLine = -1, distanceFromStart = 0, isLegitMirrorSoFar = false) => {
    if (startLine === 0 || startLine > array.length - 2) {
        return true
    }
    if (distanceFromStart === 0) {
        return checkMirror(
            [...array.slice(0, startLine), ...array.slice(startLine + 1)],
            startLine, 1, isLegitMirrorSoFar)
    }
    if (startLine - distanceFromStart < 0 || startLine + distanceFromStart >= array.length) {
        return isLegitMirrorSoFar
    }
    if (array[startLine - distanceFromStart] === array[startLine + distanceFromStart]) {
        return checkMirror(array, startLine, distanceFromStart + 1, true)
    }
    else {
        return false
    }
}

